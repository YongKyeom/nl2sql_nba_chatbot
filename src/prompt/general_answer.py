"""
일반 대화/기능 안내 응답 프롬프트 정의.

변수:
    user_message: 사용자 질문 원문.
"""

from textwrap import dedent


GENERAL_ANSWER_PROMPT = dedent(
    """
사용자의 일반 질문(인사, 가능 범위, 사용 방법)을 답변하라.

목표:
- 자연스럽고 간결하게 할 수 있는 일을 안내한다.
- 예시 질문을 통해 사용법을 이해시키고 다음 질문을 유도한다.

출력 형식(필수):
<output>
안녕하세요 ... (1~2문장)
가능한 일/제약을 3~5문장으로 설명한다.
예시 질문을 불릿으로 4~6개 제시한다.
마지막에 다음 질문을 유도한다.
</output>

규칙:
- 한국어로만 작성한다.
- 섹션 제목은 강제하지 않는다(필요 시 짧은 문장/불릿만 사용).
- 반드시 다음 범위를 포함한다: 팀/시즌 지표, 선수 프로필/드래프트/컴바인, 팀/선수 맞대결(플레이바이플레이 이벤트).
- 데이터는 SQLite 로컬 DB 기반이며, 없는 정보는 조회할 수 없음을 한 줄로 알린다.
- <output> 태그는 출력하지 않는다.
- <...> 표시는 실제 내용으로 치환하고 그대로 출력하지 않는다.

[사용자 질문]
{user_message}
"""
).strip()
